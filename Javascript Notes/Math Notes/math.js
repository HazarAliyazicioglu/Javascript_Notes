console.log(Math.round(3.6)); // En yakın değere yuvarlar

console.log(Math.ceil(4.6)); // En yakın üst değere yuvarlar

console.log(Math.floor(6.6)); // En yakın alt değere yuvarlar

console.log(Math.pow(5,2)); // x,y olacak şekilde x üzeri y işlemi yapar 

console.log(Math.min(5,2)); // Sayılar arasından en küçük değeri seçer

console.log(Math.max(5,2)); // Sayılar arasından en büyük değeri seçer 

console.log(Math.random()); // 0 ile 1 arasında rastgele sayı üretir 
